public class PrecedenciaOperadoresAtribuicao{
	public static void main(String[] args) {
		Integer numero = 5;

		
		System.out.println(numero);

		++ numero;
		System.out.println(numero);

		numero ++;
		System.out.println(numero);

		-- numero;
		System.out.println(numero);

		numero --;
		System.out.println(numero);




	}
}